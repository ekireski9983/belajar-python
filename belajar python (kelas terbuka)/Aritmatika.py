# operasi aritmatika
a = 10
b = 3
# operasi tambah +
hasil = a + b
print(a, '+',b, '=', hasil)
print("hasil penjumlahan")

# operasi kurang -
hasil = a - b
print(a, '-',b, '=', hasil)
print("Hasil pengurangan")

# operasi perkalian
hasil = a * b
print(a, '*',b, '=', hasil)
print("hasil perkalian")

# operasi pembagian /
hasil = a / b
print(a, '/',b, '=', hasil)
print("hasil pembagian")

# operasi eksponen (pangkat) **
hasil = a ** b
print(a, '**',b,'=',hasil)
print("hasil pangkat")

# operasi division //
hasil = a // b
print(a, '//',b,'=',hasil)
print("hasil division")

# prioritas operasi
x = 100
y = 2
z = 4
hasil = x ** y * z + x / y - y % z // x
print(x, '**' ,y, '*' ,z, '+' ,x, '/' ,y, '-' ,y, '%' ,z, '//',x, '=',hasil)

hasil = x + y + z
print(x, '+',y,'*',z, '=',hasil)
# kurung akan mengambil langkah paling pertama
hasil = (x + y) * z

