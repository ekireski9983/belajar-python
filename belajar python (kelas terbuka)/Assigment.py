# Operasi yang dapat dilakukan dengan penyingkatan

a = 5 # assigment
print("nilai a =",a)

a += a + 1
print("nilai a += 1, nilai a menjadi",a)

a -= 2 ## artinya a = a - 2
print("nilai a -= 2, nilai a menjadi",a)

a *= 2 ## artinya a = a * 2
print("nilai a *= 2, nilai a menjadi",a)

a /= 2 ## artinya a = a / 2
print("nilai a /= 2, nilai a menjadi",a)

b = 10
print("\nnilai b =",b)
print()

b %= 3 ## artinya b %= 3 
print("nilai a %= 3, nilai a menjadi",b)

b //= 3 
print("nilai a //= 3, nilai a menjadi",b)

